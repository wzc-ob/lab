    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
             String str = jTextField1.getText().trim();
        if ("".equals(str)) {
            JOptionPane.showMessageDialog(this, "请输入路径名");
        } else {                          
		String path = jTextField1.getText().toString();
		File file = new File(path);
                if(!file.exists()){
                    JOptionPane.showMessageDialog(this, "该路径不存在");
                }else{
		LinkedList<File> list = new LinkedList<File>();
		File[] fs =file.listFiles();
		for (File f:fs) {
			if(f.isDirectory()) {
				jTextArea1.append("文件夹：" +f.getAbsolutePath()+"上级路径"+f.getParent()+"\n");
                                DaoFactory.igetFilefolder().insert_two(f.getParent(), f.getAbsolutePath());
				list.add(f);
				
			}else {
                            if(f.getName().split("\\.").length==1){
                                jTextArea1.append("上级路径"+f.getParent()+"最后修改时间"+new Date(f.lastModified())+"文件大小"+f.length()+"文件名"+f.getName().split("\\.")[0]+"\n");
				DaoFactory.igetFilefolder().insert(f.getAbsolutePath(),new Date(f.lastModified()), f.length(), f.getName().split("\\.")[0],null);				
				DaoFactory.igetFilefolder().insert_two(f.getParent(), f.getAbsolutePath());
                            }else{
				jTextArea1.append("上级路径"+f.getParent()+"最后修改时间"+new Date(f.lastModified())+"文件大小"+f.length()+"文件名"+f.getName().substring(0, f.getName().lastIndexOf("."))+"文件类型"+f.getName().substring(f.getName().lastIndexOf("."))+"\n");
				DaoFactory.igetFilefolder().insert(f.getAbsolutePath(),new Date(f.lastModified()), f.length(), f.getName().substring(0, f.getName().lastIndexOf(".")), f.getName().substring(f.getName().lastIndexOf(".")));				
				DaoFactory.igetFilefolder().insert_two(f.getParent(), f.getAbsolutePath());   
                            }                             
			}
		}
                
		File temp_file;
		while(!list.isEmpty()) {
			temp_file = list.removeFirst();
			fs = temp_file.listFiles();
			for (File f:fs) {
				if(f.isDirectory()) {
                                        jTextArea1.append("文件夹：" +f.getAbsolutePath()+"上级路径"+f.getParent()+"\n");					
					DaoFactory.igetFilefolder().insert_two(f.getParent(), f.getAbsolutePath());
                                        list.add(f);
				}else {
					if(f.getName().split("\\.").length==1){
                                jTextArea1.append("上级路径"+f.getParent()+"最后修改时间"+new Date(f.lastModified())+"文件大小"+f.length()+"文件名"+f.getName().split("\\.")[0]+"\n");
				DaoFactory.igetFilefolder().insert(f.getAbsolutePath(),new Date(f.lastModified()), f.length(), f.getName().split("\\.")[0],null);				
				DaoFactory.igetFilefolder().insert_two(f.getParent(), f.getAbsolutePath());
                            }else{
				jTextArea1.append("上级路径"+f.getParent()+"最后修改时间"+new Date(f.lastModified())+"文件大小"+f.length()+"文件名"+f.getName().substring(0, f.getName().lastIndexOf("."))+"文件类型"+f.getName().substring(f.getName().lastIndexOf("."))+"\n");
				DaoFactory.igetFilefolder().insert(f.getAbsolutePath(),new Date(f.lastModified()), f.length(), f.getName().substring(0, f.getName().lastIndexOf(".")), f.getName().substring(f.getName().lastIndexOf(".")));				
				DaoFactory.igetFilefolder().insert_two(f.getParent(), f.getAbsolutePath());   
                            }      
			}
		}
                }
                fileModel = new FileTableModel();
                jTable1.setModel(fileModel);
                }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
