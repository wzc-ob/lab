    private void deleteButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButton3ActionPerformed
        int rowNum = jTable1.getSelectedRow();
        String route = (String) jTable1.getValueAt(rowNum, 0);
        if (rowNum >= 0) {
            int choise = JOptionPane.showConfirmDialog(this, "是否要删除该条信息", "提示", JOptionPane.YES_NO_OPTION);
            if (choise == JOptionPane.YES_OPTION) {
                if (!DaoFactory.igetFilefolder().delete(route)) {
                    JOptionPane.showMessageDialog(this, "删除失败");
                }
                fileModel = new FileTableModel();
                jTable1.setModel(fileModel);
            }
        } else {
            JOptionPane.showMessageDialog(this, "请选择要删除信息！");
        }// TODO add your handling code here:
    }//GEN-LAST:event_deleteButton3ActionPerformed
